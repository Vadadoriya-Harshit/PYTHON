# logical operator

# and (if the both the statements are true it returns TRUE)
x=83
print(x<90 and x>50)
# or (if one of the statements is true it returns TRUE)
print(x>100 or x==100)
# not (it reverses the result, it returns FALSE if it is TRUE)
print(not(x<90 and x>50))
